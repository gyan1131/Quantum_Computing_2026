"""Now, let's import the necessary libraries and define the log-prior, log-likelihood, and log-probability functions required for the MCMC sampler. The `log_prior` function defines our beliefs about the parameters before seeing the data, and the `log_likelihood` connects our model to the observed data, taking into account the measurement errors."""

import emcee
import corner

# Define the log-prior function, now accepting T2star prior bounds
def log_prior(params, t2star_min, t2star_max):
    A, T2star, freq, phi, B = params
    # Prior for A: Uniform between -2 and 2 (amplitude)
    if not -2 < A < 2: return -np.inf
    # Prior for T2star: Uniform between specified min/max
    if not t2star_min < T2star < t2star_max: return -np.inf
    # Prior for freq: Uniform around the expected frequency range
    if not 0 < freq < 5e6: return -np.inf # e.g., 0 to 5 MHz
    # Prior for phi: Uniform between -pi and pi
    if not -np.pi < phi < np.pi: return -np.inf
    # Prior for B: Uniform between -2 and 2 (offset)
    if not -2 < B < 2: return -np.inf

    return 0.0 # Return 0 for a flat prior within the bounds

# Define the log-likelihood function (remains unchanged)
def log_likelihood(params, x, y, yerr):
    A, T2star, freq, phi, B = params
    model = t2_ramsey_fit_func(x, A, T2star, freq, phi, B)
    # Assuming Gaussian errors, the log-likelihood is:
    return -0.5 * np.sum(((y - model) / yerr)**2 + np.log(2 * np.pi * yerr**2))

# Define the log-probability function, now passing T2star prior bounds to log_prior
def log_prob(params, x, y, yerr, t2star_min, t2star_max):
    lp = log_prior(params, t2star_min, t2star_max)
    if not np.isfinite(lp): # If prior is -inf, then posterior is -inf
        return -np.inf
    return lp + log_likelihood(params, x, y, yerr)

