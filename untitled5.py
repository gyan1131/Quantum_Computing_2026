# Define a function to extract P(0), its standard error, and xval from each row
def extract_p0_and_error_and_xval(row):
    counts = row['counts']
    xval = row['metadata']['xval']
    # Calculate P(0) assuming T1 measures state 0 population
    shots_0 = counts.get('0', 0)
    shots_1 = counts.get('1', 0)
    total_shots_per_circuit = shots_0 + shots_1

    p0 = shots_0 / total_shots_per_circuit if total_shots_per_circuit > 0 else 0

    # Calculate binomial standard error for p0
    # Standard error of the mean (for proportion) = sqrt(p * (1-p) / N)
    if total_shots_per_circuit > 0:
        std_err_p0 = np.sqrt(p0 * (1 - p0) / total_shots_per_circuit)
    else:
        std_err_p0 = np.finfo(float).eps # Use a small epsilon if no shots or p0 is 0 or 1

    return pd.Series({'p0': p0, 'std_err_p0': std_err_p0, 'xval': xval})

# Apply the function to the DataFrame
extracted_data = df_raw.apply(extract_p0_and_error_and_xval, axis=1)

# Extract delays, processed_y_data, and y_errors directly from extracted_data
delays = extracted_data['xval'].values
processed_y_data = extracted_data['p0'].values
y_errors = extracted_data['std_err_p0'].values

# Ensure y_errors has no zeros or NaNs for curve_fit
y_errors[y_errors == 0] = np.finfo(float).eps
y_errors[np.isnan(y_errors)] = np.finfo(float).eps

# Define the T1 fit function
def t1_fit_func(t, A, T1, B):
    return A * np.exp(-t / T1) + B

# Re-fit the processed data to get A, t1, B if not directly available from fit_result
# Initial guess for parameters (A, T1, B)
initial_guess_A = processed_y_data[0] - processed_y_data[-1]
initial_guess_B = processed_y_data[-1]

# T1: from the fit_result if available, otherwise a rough guess like max_delay / 3
initial_guess_T1 = fit_result.value.nominal_value if hasattr(fit_result, 'value') and fit_result.value.nominal_value > 0 else np.max(delays) / 3

# Ensure T1 is positive for exponential decay
if initial_guess_T1 <= 0:
    initial_guess_T1 = np.max(delays) / 3 # Fallback to a reasonable positive guess

p0 = [initial_guess_A, initial_guess_T1, initial_guess_B]

# Perform the curve fit
try:
    popt, pcov = curve_fit(t1_fit_func, delays, processed_y_data, p0=p0, sigma=y_errors, absolute_sigma=True)
    A, t1_value, B = popt
except RuntimeError as e:
    print(f"Error during curve fitting: {e}")
    # Fallback if fitting fails, perhaps raise an error or use default values
    raise

# Calculate fitted values using the delays and fit parameters
fitted_y_data = t1_fit_func(delays, A, t1_value, B)

